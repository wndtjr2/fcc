서비스 향상을 위해서 고객님의 개인정보를 위탁업체에 수집, 취급, 관리 등을 위탁하고 있으며, 관계법령에 따라 위탁계약 시 개인정보가 안전하게 관리될 수 있도록 규정하고 있습니다.
- 수탁자, 수탁범위 등에 관한 사항을 서면, 전자우편, 전화 또는 홈페이지를 통해 게시
- 위탁계약 시 서비스제공자의 개인정보보호 관련 지시엄수, 개인정보에 관한 비밀유지, 제3자 제공의 금지 및 사고시의 책임부담, 위탁기간, 처리 종료후의 개인정보의 반환 또는 파기 등을 규정하고 당해 계약내용을 서면 또는 전자적으로 보관


<strong>7. 개인정보의 제 3자 제공</strong>


FCCTV은 이용자의 개인정보를 '제 1항 개인정보의 수집항목 및 이용목적'에서 고지한 범위내에서 사용하며, 개인정보를 제3자에 제공해야 하는 경우 법령에 따른 동의를 받고 있습니다. 단 다음의 경우는 예외로 하고 있습니다.
-  FCCTV의 이용약관을 위배하거나  FCCTV의 서비스를 이용하여 타인에게 피해를 주거나 미풍양속을 해치는 위법행위를 한 사람 등에게 법적인 조치를 취하기 위해 개인 정보를 공개해야 한다고 판단되는 경우
- 법령의 규정에 의거하거나, 수사 목적으로 법령에 정해진 절차와 방법에 따라 수사기관의 요구가 있는 경우
- 통계 작성, 학술연구, 시장 조사, 정보 제공의 경우로서 특정 개인을 식별할 수 없는 형태로 제공되는 경우


<strong>8. 개인 아이디와 비밀번호 관리</strong>


고객님이 사용하고 계시는 ID와 비밀번호는 원칙적으로 고객님만이 사용하도록 되어 있습니다.
FCCTV의 고의 또는 과실이 없는 경우에, 고객님의 ID와 비밀번호 도용 또는 기타 타인의 사용으로 의해 발생된 문제에 대하여  FCCTV이 책임지지 않습니다. 어떠한 경우에도 비밀번호는 타인에게 알려 주지 마시고 로그온 상태에서는 주위의 다른 사람에게 개인 정보가 유출되지 않도록 특별한 주의를 기울여 주시기 바랍니다.
타인의 개인정보를 도용하여 고객님 가입 또는 구매가 확인되었을 경우에는 이용계약이 일방적으로 해지될 수 있으며, 주민등록법에 의해 3년 이하의 징역 또는 1천만원 이하의 벌금이 부과될 수 있습니다.


