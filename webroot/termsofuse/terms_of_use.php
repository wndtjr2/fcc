<strong>개인정보의 파기절차 및 방법</strong>


1) 파기절차
FCCTV는 개인정보 보유 및 이용기간에 명시한 기간이 경과한 경우 당해 개인정보를 지체 없이 파기합니다. 단, 회사는 거래관계가 종료 또는 이용계약해지 이후에도 불량 이용자의 재가입 방지, 법적 분쟁 해결, 수사협조, 민원처리 및 법령상 의무이행 등을 위하여 고객의 개인정보를 필요한 기간 동안 보유·이용할 수 있습니다.

2) 파기방법
종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기합니다. 전자적 파일형태로 저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제하며, 어떠한 용도로도 열람 또는 이용될 수 없도록 처리합니다. 단, 이때 기록을 재생할 수 없는 기술적 방법은 사회통념상 현재의 기술수준에서 적절한 비용이 소요되는 방법을 말합니다. 해당 개인정보의 처리가 제3자에게 위탁된 경우에는 수탁자가 더 이상 해당 개인정보를 처리하지 못하도록 합니다.

3) 이용하지 아니하는 고객의 개인정보의 파기 등

회사는 다른 법령에서 별도의 기간을 정하고 있거나 고객의 요청이 있는 경우를 제외하면, 법령에서 정의하는 기간동안 재이용하지 아니하는 회원의 개인정보를 파기하거나 다른 회원의 개인정보와 분리하여 별도로 저장·관리합니다.단, 기간 만료 30일 전까지 개인정보가 파기되거나 분리되어 저장·관리되는 사실과 기간 만료일 및 해당 개인정보의 항목을 전자우편·서면·모사전송·전화 또는 이와 유사한 방법 중 어느 하나의 방법으로 회원에게 알립니다.


<strong>3. 쿠키(Cookie)운영</strong>


쿠키는 웹사이트가 사용자의 컴퓨터 브라우저(인터넷 익스플로러, 크롬, 파이어폭스 등)에 보내는 조그마한 데이터로서 고객님이 FCCTV에 로그온한 상태에서 고객님을 인증하기 위해 사용되고 있습니다.
쿠키는 고객님의 컴퓨터는 식별하지만 고객님을 개인적으로 식별하지는 않습니다. 또한 고객님은 쿠키에 대한 선택권이 있습니다.
웹브라우저 상단의 도구 > 인터넷옵션 탭(option tab)에서 모든 쿠키를 다 받아들이거나, 쿠키가 설치될 때 통지를 보내도록 하거나, 아니면 모든 쿠키를 거부할 수 있는 선택권을 가질 수 있습니다.
고객님께서 쿠키를 거부하겠다고 선택하는 경우 사용자 등록을 요하는 서버의 서비스는 이용 할 수 없음을 감안하시기 바랍니다.


<strong>4. 기술적, 관리적 대책</strong>


① 기술적 대책
FCCTV는 고객님의 개인정보를 취급함에 있어 개인정보가 분실, 도난, 누출, 변조 또는 훼손 되지 않도록 안전성확보를 위하여 다음과 같은 기술적 대책을 강구하고 있습니다.

- 고객님의 개인정보는 비밀번호에 의해 보호되며 파일 및 전송데이터를 암호화하거나 파일잠금기능(lock)을 사용하여 중요한 데이터는 별도의 보안기능을 통해 보호되고 있습니다.
-  FCCTV는 백신프로그램을 이용하여 컴퓨터바이러스에 의한 피해를 방지하기 위한 조치를 취하고 있습니다.
백신프로그램은 주기적으로 업데이트되며 갑작스런 바이러스가 출현할 경우 백신이 나오는 즉시 이를 제공함으로써 개인정보가 침해되는 것을 방지하고 있습니다.
-  FCCTV는 암호알고리즘을 이용하여 네트워크 상의 개인정보를 안전하게 전송할 수 있는 보안장치(SSL 또는 SET)를 채택하고 있습니다.
- 해킹 등 외부침입에 대비하여 각 서버마다 침입차단시스템 및 취약점 분석시스템 등을 이용하여 보안에 만전을 기하고 있습니다.

② 관리적 대책
-  FCCTV는 고객님의 개인정보에 대한 접근권한을 최소한의 인원으로 제한하고 있으며, 개인정보를 취급하는 직원을 대상으로 새로운 보안기술 습득 및 개인정보보호의무 등에 관해 정기적인 사내 교육 및 외부 위탁교육을 실시하고 있습니다.
- 입사 시 전 직원의 보안서약서를 통하여 사람에 의한 정보유출을 사전에 방지하고 개인정보취급방침에 대한 이행사항 및 직원의 준수여부를 감시하기 위한 내부절차를 마련하고 있습니다.
- 개인정보 관련 취급자의 업무 인수인계는 보안이 유지된 상태에서 철저하게 이루어지고 있으며 입사 및 퇴사 후 개인정보 사고에 대한 책임을 명확화하고 있습니다.
- 개인정보와 일반 데이터를 혼합하여 보관하지 않고 별도의 서버를 통해 분리하여 보관 하고 있습니다.
- 전산실 및 자료 보관실 등을 특별 보호구역으로 설정하여 출입을 통제하고 있습니다.
-  FCCTV는 관리적 보호대책을 다 하였음에도 불구하고 이용자 개인의 실수로 인해 일어나는 일들에 대해 책임을 지지않습니다. 고객님 개개인의 본인의 개인정보를 보호하기 위해서 자신의 ID와 비밀번호를 적절하게 관리해야합니다.
- 그 외 내부 관리자의 실수나 기술관리 상의 사고로 인해 개인정보의 상실, 유출, 변조, 훼손이 유발될 경우  FCCTV는 즉각 고객님께 사실을 알리고 적절한 대책과 보상을 강구할 것 입니다.
- 개정된 정보통신망 이용촉진 및 정보보호 등에 관한 법률에 따라 다음과 같이 고객 개인정보보호활동을 강화하고 있습니다.

